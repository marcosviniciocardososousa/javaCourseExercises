package relacaoNparaN2;

public class NotaFilme {
	
	/*
	 * nota filme serve somente para armazenar o resultado 
	 * da media dos filmes.
	 */
	
	private double media;

	public NotaFilme(double media) {
		super();
		this.media = media;
	}

	public double getMedia() {
		return media;
	}

	public void setMedia(double media) {
		this.media = media;
	}
	
	

}
